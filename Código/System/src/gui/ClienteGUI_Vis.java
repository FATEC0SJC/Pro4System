    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
            JTable source = (JTable)evt.getSource();
            int row = source.rowAtPoint( evt.getPoint() );
            String nome =source.getModel().getValueAt(row, 0)+"";
            String empresa =source.getModel().getValueAt(row, 1)+"";
            String cargo =source.getModel().getValueAt(row, 2)+"";
            String telefone =source.getModel().getValueAt(row, 3)+"";
            String email =source.getModel().getValueAt(row, 4)+"";
            
            Cliente clienteSelected = new Cliente(nome, empresa, cargo, telefone, email);
            ClientePageGUI clientePage = new ClientePageGUI();
            clientePage.setVisible(true);
            clientePage.getCliente(clienteSelected);
            clientePage.setExtendedState(clientePage.getExtendedState() | JFrame.MAXIMIZED_BOTH);
            dispose();
    }//GEN-LAST:event_jTable1MouseClicked

