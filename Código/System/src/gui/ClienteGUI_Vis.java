    public void readJTable() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        
        modelo.setNumRows(0);
        
        ClienteDAO clienteDao = new ClienteDAO();
        
        for(Cliente c: clienteDao.read()){
            modelo.addRow(new Object[]{
                c.getNome(),
                c.getEmpresa(),
                c.getCargo(),
                c.getTelefone(),
                c.getEmail()
            });
        }
    }
    
    public void searchJTable(String nome, String empresa, String cargo) throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        
        modelo.setNumRows(0);
        
        ClienteDAO clienteDao = new ClienteDAO();
        
        for(Cliente c: clienteDao.search(nome, empresa, cargo)){
            modelo.addRow(new Object[]{
                c.getNome(),
                c.getEmpresa(),
                c.getCargo(),
                c.getTelefone(),
                c.getEmail()
            });
        }
    }
    
