    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());

        String nome = source.getModel().getValueAt(row, 0) + "";
        String empresa = source.getModel().getValueAt(row, 1) + "";
        String cliente = source.getModel().getValueAt(row, 2) + "";
        int id = (int) jTable1.getModel().getValueAt(row, 3);

        Projeto projetoSelected = new Projeto(nome, empresa, cliente, id);
        ProjetoPageGUI projetoPage = new ProjetoPageGUI(projetoSelected);
        projetoPage.setVisible(true);
        projetoPage.setExtendedState(projetoPage.getExtendedState() | JFrame.MAXIMIZED_BOTH);
        dispose();
    }//GEN-LAST:event_jTable1MouseClicked

