    public void searchJTable(String nome, String empresa, Cliente cliente) throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        
        modelo.setNumRows(0);
        
        ProjetoDAO projetoDao = new ProjetoDAO();
        ClienteDAO clienteDao = new ClienteDAO();
        if (cliente == null) {
            cliente = new Cliente();
            cliente.setEmail("");
        }
        for (Projeto c : projetoDao.search(nome, empresa, cliente.getEmail())) {
            if (empresa == null) {
            modelo.addRow(new Object[]{
                c.getNome(),
                c.getEmpresa(),
                clienteDao.searchByEmail(c.getCliente()).get(0),
                c.getId()
            });
            } else {
                if (c.getEmpresa().equals(empresa)) {
                modelo.addRow(new Object[]{
                c.getNome(),
                c.getEmpresa(),
                clienteDao.searchByEmail(c.getCliente()).get(0),
                c.getId()
                });
            }
        }
        }
    }

    public void readJTable() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        modelo.setNumRows(0);
        TableColumnModel tcm = jTable1.getColumnModel();
        tcm.removeColumn(tcm.getColumn(3));

        ProjetoDAO projetoDao = new ProjetoDAO();
        ClienteDAO clienteDao = new ClienteDAO();

        for (Projeto c : projetoDao.read()) {
            modelo.addRow(new Object[]{
                c.getNome(),
                c.getEmpresa(),
                clienteDao.searchByEmail(c.getCliente()).get(0),
                c.getId()
            });
        }
    }
    
